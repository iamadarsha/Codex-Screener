// Starts with all NIFTY 500; API results merge in later
let _stockCache: NseStock[] = [...NIFTY_500];

export function setStockCache(stocks: NseStock[]) {
  const existing = new Set(_stockCache.map((s) => s.symbol));
  const newStocks = stocks.filter((s) => !existing.has(s.symbol));
  _stockCache = [..._stockCache, ...newStocks];
}

export function searchLocalStocks(query: string, limit = 10): NseStock[] {
  if (!query || query.length < 1) return [];
  const q = query.toUpperCase();

  // Prioritize: exact symbol prefix > symbol contains > name contains
  const exactPrefix: NseStock[] = [];
  const symbolContains: NseStock[] = [];
  const nameContains: NseStock[] = [];

  for (const s of _stockCache) {
    const sym = s.symbol.toUpperCase();
    const name = s.name.toUpperCase();
    if (sym.startsWith(q)) {
      exactPrefix.push(s);
    } else if (sym.includes(q)) {
      symbolContains.push(s);
    } else if (name.includes(q)) {
      nameContains.push(s);
    }
  }

  return [...exactPrefix, ...symbolContains, ...nameContains].slice(0, limit);
}
